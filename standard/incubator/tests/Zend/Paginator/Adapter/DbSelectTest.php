class Zend_Paginator_Adapter_DbSelectTest extends PHPUnit_Framework_TestCase
{
    /**
     * @var Zend_Paginator_Adapter_DbSelect
     */
    private $_adapter;
    
    private $_db;
    
    /**
     * Prepares the environment before running a test.
     */
    protected function setUp ()
    {
        parent::setUp();
        
        $this->_db = new Zend_Db_Adapter_Pdo_Sqlite(array(
            'dbname' => 'Paginator/_files/test.sqlite'
        ));
        $query = $this->_db->select()->from('test');
        
        $this->_adapter = new Zend_Paginator_Adapter_DbSelect($query);
    }
    /**
     * Cleans up the environment after running a test.
     */
    protected function tearDown ()
    {
        $this->_adapter = null;
        parent::tearDown();
    }
    
    public function testGetItemsOffsetZero()
    {
        $actual = $this->_adapter->getItems(0, 10);
        
        $i = 1;
        foreach ($actual as $item) {
        	$this->assertEquals($i, $item->number);
        	$i++;
        }
    }
    
    public function testGetItemsOffsetTen()
    {
        $actual = $this->_adapter->getItems(10, 10);
        
        $i = 11;
        foreach ($actual as $item) {
            $this->assertEquals($i, $item->number);
            $i++;
        }
    }
    
    public function testSetIntRowCount()
    {
        $this->_adapter->setRowCount(101);
        $this->assertEquals(101, $this->_adapter->count());
    }
    
    public function testSetInvalidQueryRowCount()
    {
        try {
            $this->_adapter->setRowCount($this->_db->select()->from('test'));
        } catch (Exception $e) {
            $this->assertType('Zend_Paginator_Exception', $e);
            $this->assertContains('Row count column not found', $e->getMessage());
        }
    }
    
    public function testSetValidQueryRowCount()
    {
        $this->_adapter->setRowCount($this->_db
                                          ->select(new Zend_Db_Expr('COUNT(*) AS '
                                                                  . self::ROW_COUNT_COLUMN))
                                          ->from('test'));
    }
    
    public function testSetInvalidRowCount()
    {
        try {
            $this->_adapter->setRowCount('invalid');
        } catch (Exception $e) {
            $this->assertType('Zend_Paginator_Exception', $e);
            $this->assertContains('Invalid row count', $e->getMessage());
        }
    }
    
    public function testCountWithAutoGeneratedQuery()
    {
        $expected = 500;
        $actual = $this->_adapter->count();
        
        $this->assertEquals($expected, $actual);
    }
}